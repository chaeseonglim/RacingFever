package com.lifejourney.racingfever;

public class Rect {

    public Rect() {
    }

    public Rect(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public int x = 0;
    public int y = 0;
    public int width = 0;
    public int height = 0;
}
